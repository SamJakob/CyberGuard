/// Test if a specified String [value] matches a specified [regex].
/// Optionally, you may specify [caseSensitive], which defaults to false to
/// ignore case.
bool matches(final String value,
    {required final String regex, final bool caseSensitive = false}) {
  return RegExp(
    regex,
    multiLine: false,
    caseSensitive: caseSensitive,
  ).hasMatch(value);
}
